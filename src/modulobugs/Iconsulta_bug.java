    private void bt_consultar_bugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_consultar_bugActionPerformed

        String id = txt_id_bug.getText();
        
        try {
            
            ConnectDB db = new ConnectDB();
            BugManager manager = new BugManager();
            
            ResultSet rs = manager.ConsultarBug(Integer.parseInt(id));
                
            while (rs.next()) {   
                txt_titulo_bug.setText(rs.getString("titulo"));
                txt_status_bug.setSelectedItem(rs.getString("status"));
                txt_descricao_bug.setText(rs.getString("descricao"));
                txt_passos_reproducao.setText(rs.getString("reproducao"));
                txt_filename.setText(rs.getString("file"));
                txt_classificacao_bug.setSelectedItem(rs.getString("classificacao"));
            }
            
            if (!rs.next() && txt_titulo_bug.getText().equals(""))
                JOptionPane.showMessageDialog(null, "Nenhum bug encontrado para o id: " + id);
                
        } catch (SQLException ex) {
            Logger.getLogger(Iconsulta_bug.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bt_consultar_bugActionPerformed

