    private void bt_excluir_bugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_excluir_bugActionPerformed
        // TODO add your handling code here:
        
        try {
              
            int id = Integer.parseInt(txt_id_bug.getText());
            
            BugManager manager = new BugManager();
        
            this.bt_consultar_bugActionPerformed(evt);
            
            int confirm = JOptionPane.showConfirmDialog(null, "Deseja excluir o bug?");

            if (confirm == 0)
            {   
                manager.ExcluirBug(id);                
                this.btn_limpar_campos_bugActionPerformed(evt);
            }
            
        } 
        catch (Exception e)
        {
            //JOptionPane.showMessageDialog(null, "Informe o ID do bug que deseja excluir!");
            System.out.println(e);
        }
        
    }//GEN-LAST:event_bt_excluir_bugActionPerformed

